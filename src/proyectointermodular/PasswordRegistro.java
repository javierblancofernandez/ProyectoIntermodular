    private void registroUserPasswordjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroUserPasswordjButtonActionPerformed
        // TODO add your handling code here:
        UsuarioPassword userReg = new UsuarioPassword();
        userReg.setUsuarioNombre(usuarioRegistrojTextField.getText());
        userReg.setUsuarioEmail(emailRegistrojTextField.getText());
        //userReg.setUsuarioPassword(contrasenaRegistrojTextField.getText());
        // Para encriptar el password en la BBDD.
        EncriptadoCipher encript = new EncriptadoCipher();
        String encriptada = encript.encriptar(contrasenaRegistrojTextField.getText(), pass);
        userReg.setUsuarioPassword(encriptada);
        base.insertUsuarioPassword(userReg);
        JOptionPane.showMessageDialog(this, "El usuario"+usuarioRegistrojTextField.getText()+" ha sido registrado a la BBDD");
        // Creo el hilo para enviar el email al usuario que se ha registrado
        Thread userEmailThread = new Thread(new EmailSender(emailRegistrojTextField.getText(), 
                                                        "Bienvenido " + usuarioRegistrojTextField.getText(), 
                                                        "Tu registro ha sido exitoso."));
        // Creo el hilo para enviar el email al administrador con el usuario que se ha registrado
        Thread adminEmailThread =new Thread(new EmailSender("admin@example.com", "Nuevo usuario registrado", "Se ha registrado el usuario: " + usuarioRegistrojTextField.getText()));
                System.out.println("Correo enviado al administrador.");

        // Iniciar los hilos
        userEmailThread.start();
        adminEmailThread.start();
        
        try {
            userEmailThread.join();
            adminEmailThread.join();
        } catch (InterruptedException ex) {
            Logger.getLogger(PasswordRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        System.out.println("este es el nombre del hilo"+userEmailThread.getName());
        System.out.println("este es el nombre del hilo"+adminEmailThread.getName());
    }//GEN-LAST:event_registroUserPasswordjButtonActionPerformed

